        double b = 65;

        System.out.println("Biggest number  :- "+Math.max(a,b));
        System.out.println("Square root     :- "+Math.sqrt(a));
        System.out.println("Power of        :- "+Math.pow(a,b));
        System.out.println("Logarithum      :- "+Math.log10(a));
        System.out.println("Log offf ;)     :- "+Math.log(a));
        System.out.println("sin             :- "+Math.sin(1));
        System.out.println("cos             :- "+Math.cos(b));
